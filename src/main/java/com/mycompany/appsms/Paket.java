    private Gson gson = new Gson();

    @Override
    public void onMessage(String message) {
        JsonObject json = gson.fromJson(message, JsonObject.class);
        if(json.get("type").getAsString().equals("success")){
            String msg = json.get("message").getAsString();
            JOptionPane.showMessageDialog(this, msg);
        }else if(json.get("type").getAsString().equals("error")){
            String msg = json.get("message").getAsString();
            JOptionPane.showMessageDialog(this, msg);
        }else if(json.get("type").getAsString().equals("notification")) {
            String msg = json.get("message").getAsString();
            jTextAreaLog.setAutoscrolls(true);
            if(json.get("success").getAsBoolean()){
                String operator, nominal, nomor;
                operator = (String) Operator.getSelectedItem();
                nominal = (String) Nominal.getSelectedItem();
                nomor = (String) jTextFieldTo.getText();
                
                jTextAreaLog.append("Laporan pengiriman sukses : " + msg);
            try{java.sql.Statement statement = Koneksi.getConnection().createStatement();
                statement.executeUpdate("INSERT INTO tbl_transaksi(nama_produk, jenis_produk, Nominal, nomor, status) "
                        + "VALUES('"+operator+"','Paket Data', '"+nominal+"', '"+nomor+"', 'Sukses')");
                statement.close();
                JOptionPane.showMessageDialog(null, "Transaksi Berhasil");
            }catch(Exception e){
            }
            }else{
                String operator, nominal, nomor;
                operator = (String) Operator.getSelectedItem();
                nominal = (String) Nominal.getSelectedItem();
                nomor = (String) jTextFieldTo.getText();
                jTextAreaLog.append("Laporan pengiriman gagal : " + msg);
                try{java.sql.Statement statement = Koneksi.getConnection().createStatement();
                statement.executeUpdate("INSERT INTO tbl_transaksi(nama_produk, jenis_produk, Nominal, nomor, status, keterangan) "
                        + "VALUES('"+operator+"','Paket Data', '"+nominal+"', '"+nomor+"', 'Gagal', '"+msg+"')");
                statement.close();
                JOptionPane.showMessageDialog(null, "Upps Sepertinya ada masalah,");
            }catch(Exception e){
            }
            }
            jTextAreaLog.append("\n");
        }else if(json.get("type").getAsString().equals("received")){
            String msg = json.get("message").getAsString();
            String from = json.get("from").getAsString();
            
            int result = JOptionPane.showConfirmDialog(this, new String[]{
                "SMS dari " + from + " dengan pesan : ",
                msg,
                "Apakah ingin dibalas?"
            });
            
            if(result == JOptionPane.OK_OPTION){
                jTextFieldTo.setText(from);
//                jTextAreaMessage.setText("");
            }
        }
    }
