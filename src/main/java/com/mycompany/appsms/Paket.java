    private void jButtonSendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSendActionPerformed
        // TODO add your handling code here:
        String operator, nominal, message;
        operator = (String) Operator.getSelectedItem();
        nominal = (String) Nominal.getSelectedItem();

        message =  "Selamat Pengisian Pulsa "+operator+" Senilai "+nominal+" Berhasil.Jangan Bosan Beli di SIPULsa ya <3";
        //        //buatjson
        JsonObject object = new JsonObject();
        object.add("message", new JsonPrimitive(message));

        //cek apakah broadcast
        if(jTextFieldTo.getText().contains(",")){
            String[] tos = jTextFieldTo.getText().split(",");
            JsonArray array = new JsonArray();
            for(String to : tos){
                array.add(new JsonPrimitive(to));
            }
            object.add("to", array);
        }else {
            //bukan bc

            object.add("to", new JsonPrimitive(jTextFieldTo.getText()));
        }

        //konversi jadi  json
        String json = gson.toJson(object);

        //kirim ke server
        client.send(json);
    }//GEN-LAST:event_jButtonSendActionPerformed

